# Função print()

""" Sequência | Nome | Significado                 | Usado por padrão        |
    \n        | LF   | Line Feed (nova linha)      | Linux, macOS (Unix-like)|
    \r\n      | CRLF | Carriage Return + Line Feed | Windows               """

# sep = separador
# end = finalizador
print(12, 34, 99, sep="", end='#')
print(56, 78, sep='-', end='\n')
print(9, 10, sep='-', end='\n')
