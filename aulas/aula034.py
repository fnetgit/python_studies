# Repetições (while e brek)

# while (enquanto)
# Executa uma ação enquanto uma condição for verdadeira
# break (interrompe o loop)

condition = True

while condition:
    name = input('Qual o seu nome: ')
    print(f'Seu nome é {name}')

    if name == 'sair':
        break

print('Acabou')


