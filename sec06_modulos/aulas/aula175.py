# JSON - JavaScript Object Notation (extensão .json)

# É uma estrutura de dados que permite a serialização
# de objetos em texto simples para facilitar a transmissão de dados

# Ao converter de Python para JSON:
# Python        JSON
# dict          object
# list, tuple   array
# str           string
# int, float    number
# True          true
# False         false
# None          null
