# for com range
# range --> (start, stop, step)

numeros_de_zero_a_nove = range(10)
numeros_de_um_a_nove = range(1, 10)
numeros_impares_ate_dez = range(1, 10, 2)

for numero in numeros_de_zero_a_nove:
    print(numero)
print('')

for numero in numeros_de_um_a_nove:
    print(numero)
print('')

for numero in numeros_impares_ate_dez:
    print(numero)
