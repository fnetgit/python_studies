# Tipo de dado str

"""
•Tipagem dinâmica:
O tipo da variável é definido automaticamente em tempo de execução, sem precisar declarar.
•Tipagem estática:
Exige que você declare o tipo da variável, e o tipo é verificado em tempo de compilação.
•Tipagem forte:
Não mistura tipos automaticamente, precisa de conversão expl
•Tipagem fraca:
Mistura tipos automaticamente, sem conversão explícita.


Tipo de tipagem do Python = Dinâmica e Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print(1, 'Francisco Neto')

# Aspas duplas
print(2, "Francisco Neto")

# Aspas aparecerem
print('"Francisco Neto"')
print("'Francisco Neto'")

# Escape
print("Francisco \"Neto\"")

# r
print(r"Francisco \"Neto\"")
