# Conversão de tipos, Coerção

# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
#  tipos imutáveis e primitivos:
# str, int, float, bool
print(1 + 1)
print(int('1') + int('1'))
print(float('1') + 1)

print(bool('')) # Vazia dá False
print(bool(' ')) # Não vazia dá True

print('ben' + str(10))
